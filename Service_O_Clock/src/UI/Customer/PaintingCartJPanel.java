    private void populatePaintTable() {
        DefaultTableModel model = (DefaultTableModel) serviceTable.getModel();
        model.setRowCount(0);
            Object[] row = new Object[3];
            for(PaintingPackages req:painting.getPaintingPackageList()){
                 row[0] = req;
                 row[1] = req.getPaintDesc();
                 row[2] = req.getColor();
                 model.addRow(row);
            }  
    }
     private void populateCartTable(PaintingPackages paint) {
        DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
        model.setRowCount(0);

        servicesOff.add(paint);
        
        Object[] row = new Object[3];
        for(PaintingPackages req:servicesOff){
            row[0] = req;
            row[1] = req.getPaintDesc();
            row[2] = req.getColor();
            
            model.addRow(row);
        }  
    }

    
