    private void populateCart(Service serv) {
        DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
        model.setRowCount(0);

        servicesOff.add(serv);
        
        Object[] row = new Object[3];
        for(Service s:servicesOff){
            row[0] = s;
            row[1] = s.getServiceDescription();
            row[2] = s.getServiceType();
            
            model.addRow(row);
        }  
    }
    
    private void populateService() {
        DefaultTableModel model = (DefaultTableModel) serviceTable.getModel();
        model.setRowCount(0);
            Object[] row = new Object[3];
            for(Service ser:homeService.getServiceList()){
                 row[0] = ser;
                 row[1] = ser.getServiceDescription();
                 row[2] = ser.getServiceType();
                 model.addRow(row);
            }  
    }
    
