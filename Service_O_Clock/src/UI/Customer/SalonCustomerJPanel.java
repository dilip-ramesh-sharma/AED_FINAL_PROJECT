    private void populateSalonTable() {
        
        DefaultTableModel salonTableModel = (DefaultTableModel) orgTable.getModel();
        salonTableModel.setRowCount(0);
        
            Object[] row = new Object[4];    
            for(Salon salon:ecosystem.getSalonDirectory().getSalonsList()){
                 row[0] = salon;
                 row[1] = salon.getSalonAddress();
                 row[2] = salon.getSalonContact();
                 row[3] = salon.getSalonEmail();
                 salonTableModel.addRow(row);
            }
            
    }

