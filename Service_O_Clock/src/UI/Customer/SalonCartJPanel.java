    private void populateCart(SalonServicesOffered serv) {
        DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
        model.setRowCount(0);

        servicesOff.add(serv);
        
        Object[] row = new Object[3];
        for(SalonServicesOffered s:servicesOff){
            row[0] = s;
            row[1] = s.getServiceDescription();
            row[2] = s.getServiceType();
            
            model.addRow(row);
        }  
    }
    
    private void populateService() {
        DefaultTableModel model = (DefaultTableModel) serviceTable.getModel();
        model.setRowCount(0);
            Object[] row = new Object[3];
            for(SalonServicesOffered req:salon.getServicesOfferedList()){
                 row[0] = req;
                 row[1] = req.getServiceDescription();
                 row[2] = req.getServiceType();
                 model.addRow(row);
            }  
    }
    
